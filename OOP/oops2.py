class Automobile:

    def __init__(self,color,speed,make,Noofgears):
        self.color=color
        self.speed=speed
        self.make=make
        self.Noofgears=Noofgears
    def getcolor(self):
        return self.color
    def getspeed(self):
        return self.speed
    def getmake(self):
        return self.make
    def getNoofgears(self):
        return self.Noofgears
